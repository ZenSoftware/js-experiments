import { isValidSudoku } from './valid-sudoku';

describe('Valid Sudoku', () => {
  it('evaluates correctly 1', () => {
    const board = [
      ['5', '3', '.', '.', '7', '.', '.', '.', '.'],
      ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
      ['.', '9', '8', '.', '.', '.', '.', '6', '.'],
      ['8', '.', '.', '.', '6', '.', '.', '.', '3'],
      ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
      ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
      ['.', '6', '.', '.', '.', '.', '2', '8', '.'],
      ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
      ['.', '.', '.', '.', '8', '.', '.', '7', '9'],
    ];
    expect(isValidSudoku(board)).toEqual(true);
  });

  it('evaluates correctly 2', () => {
    const board = [
      ['8', '3', '.', '.', '7', '.', '.', '.', '.'],
      ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
      ['.', '9', '8', '.', '.', '.', '.', '6', '.'],
      ['8', '.', '.', '.', '6', '.', '.', '.', '3'],
      ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
      ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
      ['.', '6', '.', '.', '.', '.', '2', '8', '.'],
      ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
      ['.', '.', '.', '.', '8', '.', '.', '7', '9'],
    ];
    expect(isValidSudoku(board)).toEqual(false);
  });

  it('evaluates correctly 3', () => {
    const board = [
      ['.', '.', '.', '.', '5', '.', '.', '1', '.'],
      ['.', '4', '.', '3', '.', '.', '.', '.', '.'],
      ['.', '.', '.', '.', '.', '3', '.', '.', '1'],
      ['8', '.', '.', '.', '.', '.', '.', '2', '.'],
      ['.', '.', '2', '.', '7', '.', '.', '.', '.'],
      ['.', '1', '5', '.', '.', '.', '.', '.', '.'],
      ['.', '.', '.', '.', '.', '2', '.', '.', '.'],
      ['.', '2', '.', '9', '.', '.', '.', '.', '.'],
      ['.', '.', '4', '.', '.', '.', '.', '.', '.'],
    ];
    expect(isValidSudoku(board)).toEqual(false);
  });
});
